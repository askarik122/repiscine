int		ft_strcmp(char *s1, char *s2)
{
	int r;
	int i;

	r = 0;
	i = 0;
	while (s1[i] == s2[i])
	{
		if (s2[i] == '\0' && s1[i] == '\0')
			return (0);
		i++;
	}
	if (s1[i] - s2[i] > 0)
	{
		r = s1[i] - s2[i];
		return (r);
	}
	if (s2[i] - s1[i] > 0)
	{
		r = (s2[i] - s1[i]);
		return (-1 * r);
	}
	return (r);
}
